/**
 * Represents a point in 2D space with x and y coordinates.
 *
 * @remarks
 * The `Coordinate` is a tuple where the first element is the x-coordinate and the second element is the y-coordinate.
 *
 * @typeParam 0 - The x-coordinate of the point
 * @typeParam 1 - The y-coordinate of the point
 */
export type Coordinate = [number, number];
